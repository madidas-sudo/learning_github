def plusOne(arr):
    for i, j in enumerate(arr):
        arr[i] = j + 1

def divideByfour(arr):
    for i, j in enumerate(arr):
        arr[i] = j / 4

def plusThree(arr):
    for i, j in enumerate(arr):
        arr[i] = j + 3

def flatten(arr):
    for i, j in enumerate(arr):
        arr[i] = int(j)

def plusTwo(arr):
    for i, j in enumerate(arr):
        arr[i] = j + 2